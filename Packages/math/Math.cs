using UnityEngine;

namespace Coffee.Ugd
{
    public static class Math
    {
        public static float GetX(Vector3 v)
        {
            return v.x;
        }

        public static float GetY(Vector3 v)
        {
            return v.y;
        }

        public static float GetZ(Vector3 v)
        {
            return v.z;
        }
    }
}
